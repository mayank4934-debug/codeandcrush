// Frontend & Backend Quiz Data — 15 MCQ + 2 Programming Questions per part
import type {
  PartMCQ,
  PartProgrammingQuestion,
  PartQuizData,
} from "./partQuizData";

function mcq(
  id: string,
  question: string,
  options: [string, string, string, string],
  correct: 0 | 1 | 2 | 3,
  explanation: string,
): PartMCQ {
  return { id, question, options, correct, explanation, xp: 5 };
}

function pq(
  id: string,
  title: string,
  description: string,
  examples: { input: string; output: string }[],
  starterCode: string,
  languageId: number,
  languageLabel: string,
  hints: [string, string, string],
  solutionKeywords: string[],
): PartProgrammingQuestion {
  return {
    id,
    title,
    description,
    examples,
    starterCode,
    languageId,
    languageLabel,
    hints,
    solutionKeywords,
    xp: 20,
  };
}

